age = int(input("Enter your age: "))

print("\nAge classification results:")
if age >= 30:
    print("30+: Ideal phase for career transition and advanced studies in AI.")
elif age >= 18:
    print("18-29: Prime time for technical skills development and entry into tech industry.")
else:
    print("Under 18: Ample time to build strong foundations in programming and AI.")

